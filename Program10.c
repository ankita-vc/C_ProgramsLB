// Display "Jay Ganesh" 5 times on screen.

#include<stdio.h>

// Example of ITERATION(for loop) 
int main()
{
    int iCnt= 0;

//       1.       2.         3.
    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        printf("Jay Ganesh....\n");   //4.
    }

    return 0;
}


// If you know how many times you want to repeat the condition then use for loop.. 
// and if you dont know about the iteration then use while loop